#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*ptr;
	t_list	*new_lst;
	t_list	*new_elem;

	ptr = lst;
	new_lst = NULL;
	while (ptr)
	{
		new_elem = ft_lstnew((*f)(ptr->content));
		if (!new_elem)
		{
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, new_elem);
		ptr = ptr->next;
	}
	return (new_lst);
}
